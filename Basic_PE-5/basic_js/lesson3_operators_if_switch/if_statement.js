// If statement

// If statement is used to execute a block of code only if the condition is true.
{
  let x = 1;
  if (x === 1) {
    console.log("The condition is true");
  }
}

// If-else statement

// If-else statement is used to execute a block of code if the condition is true and another block of code if the condition is false.

{
  let x = 1;
  if (x === 2) {
    console.log("The condition is true");
  } else {
    console.log("The condition is false");
  }
}

// If-else if-else statement

// If-else if-else statement is used to execute a block of code if the first condition is true, another block of code if the second condition is true, and another block of code if the conditions are false.

{
  let x = 1;
  if (x === 2) {
    console.log("The first condition is true");
  } else if (x === 1) {
    console.log("The second condition is true");
  } else {
    console.log("The conditions are false");
  }
}

// Nested if statement

// Nested if statement is an if statement inside another if statement.

{
  let x = 1;
  let y = 2;
  if (x === 1) {
    if (y === 2) {
      console.log("The conditions are true");
    }
  }
}

