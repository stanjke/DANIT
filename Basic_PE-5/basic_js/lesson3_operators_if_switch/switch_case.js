// Switch case statement

// Switch case statement is used to execute a block of code depending on the value of a variable.

{
  let x = 1;
  switch (x) {
    case 1:
      console.log("The value is 1");
      break;
    case 2:
      console.log("The value is 2");
      break;
    default:
      console.log("The value is neither 1 nor 2");
      break;
  }
}
